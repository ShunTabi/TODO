CREATE TABLE T_GENRE(
GENRE_ID INTEGER PRIMARY KEY AUTOINCREMENT,
GENRE_NAME TEXT NOT NULL UNIQUE,
GENRE_DATETIME DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
GENRE_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_PRIOR(
PRIOR_ID INTEGER PRIMARY KEY AUTOINCREMENT,
PRIOR_NAME TEXT NOT NULL,
PRIOR_SUBNAME TEXT NOT NULL,
PRIOR_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
PRIOR_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_STATUS(
STATUS_ID INTEGER PRIMARY KEY AUTOINCREMENT,
STATUS_NAME TEXT NOT NULL,
STATUS_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
STATUS_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_GOAL(
GOAL_ID INTEGER PRIMARY KEY AUTOINCREMENT,
GOAL_NAME TEXT NOT NULL UNIQUE,
GENRE_ID INTEGER NOT NULL REFERENCES T_GENRE(GENRE_ID),
GOAL_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
GOAL_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_TODO_HEADER(
TODO_HEADER_ID INTEGER PRIMARY KEY AUTOINCREMENT,
GOAL_ID INTEGER NOT NULL REFERENCES T_GOAL(GOAL_ID),
PRIOR_ID INTEGER NOT NULL REFERENCES T_PRIOR(PRIOR_ID),
TODO_HEADER_NAME TEXT NOT NULL UNIQUE,
TODO_HEADER_ENDDATE DATE NOT NULL,
TODO_HEADER_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
TODO_HEADER_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_TODO_DETAIL(
TODO_DETAIL_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TODO_HEADER_ID INTEGER  NOT NULL REFERENCES T_TODO_HEADER(TODO_HEADER_ID),
STATUS_ID INTEGER NOT_NULL REFERENCES T_STATUS(STATUS_ID),
TODO_DETAIL_NAME TEXT NOT NULL,
TODO_DETAIL_STARTDATE DATE NOT NULL,
TODO_DETAIL_ENDDATE DATE NOT NULL,
TODO_DETAIL_DATETIME DATETIME DEFAULT CURRENT_TIMESTAMP,
TODO_DETAIL_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_MEMO(
MEMO_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TODO_DETAIL_ID INTEGER  NOT NULL REFERENCES T_TODO_DETAIL(TODO_DETAIL_ID),
MEMO_CONTENT TEXT NOT NULL,
MEMO_DATETIME DATE NOT NULL DEFAULT CURRENT_TIMESTAMP,
MEMO_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_SQLLOG(
SQLLOG_ID INTEGER PRIMARY KEY AUTOINCREMENT,
SQLLOG_CODE INTEGER NOT NULL,
SQLLOG_SQLSTATEMENT TEXT NOT NULL,
SQLLOG_PAR TEXT NOT NULL,
SQLLOG_DATETIME TIMESTAMP NOT NULL DEFAULT CURRENT_TIMESTAMP
);
CREATE VIEW V_GOAL AS
SELECT *FROM T_GOAL
INNER JOIN T_GENRE USING(GENRE_ID);


CREATE VIEW V_TODO_HEADER AS
SELECT * FROM T_TODO_HEADER
INNER JOIN T_GOAL USING(GOAL_ID)
INNER JOIN T_GENRE USING(GENRE_ID)
INNER JOIN T_PRIOR USING(PRIOR_ID);


CREATE VIEW V_TODO_DETAIL AS 
SELECT * FROM T_TODO_DETAIL
INNER JOIN T_STATUS USING(STATUS_ID)
INNER JOIN T_TODO_HEADER USING(TODO_HEADER_ID)
INNER JOIN T_PRIOR USING(PRIOR_ID)
INNER JOIN T_GOAL USING(GOAL_ID)
INNER JOIN T_GENRE USING(GENRE_ID);


CREATE VIEW V_MEMO AS
SELECT * FROM T_MEMO
INNER JOIN T_TODO_DETAIL USING(TODO_DETAIL_ID)
INNER JOIN T_TODO_HEADER USING(TODO_HEADER_ID)
INNER JOIN T_GOAL USING(GOAL_ID)
INNER JOIN T_GENRE USING(GENRE_ID)
INNER JOIN T_PRIOR USING(PRIOR_ID)
INNER JOIN T_STATUS USING (STATUS_ID);
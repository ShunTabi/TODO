CREATE VIEW V_GOAL AS
SELECT
    T_GOAL.GOAL_ID
    , T_GOAL.GOAL_NAME
    , T_GOAL.GOAL_DATE
    , T_GENRE.GENRE_ID
    , T_GENRE.GENRE_NAME
    , T_GENRE.GENRE_DATE
    , T_GOAL.GOAL_VISIBLESTATUS
    , T_GENRE.GENRE_VISIBLESTATUS 
FROM
    T_GOAL 
    INNER JOIN T_GENRE 
        ON T_GOAL.GENRE_ID = T_GENRE.GENRE_ID


CREATE VIEW V_MEMO AS 
SELECT
    T_MEMO.MEMO_ID
    , T_MEMO.MEMO_CONTENT
    , T_MEMO.MEMO_DATE
    , T_GOAL.GOAL_ID
    , T_GOAL.GOAL_NAME
    , T_TODO_HEADER.TODO_HEADER_ID
    , T_TODO_HEADER.TODO_HEADER_NAME
    , T_STATUS.STATUS_ID
    , T_STATUS.STATUS_NAME
    , T_TODO_DETAIL.TODO_DETAIL_ID
    , T_TODO_DETAIL.TODO_DETAIL_CONTENT
    , T_TODO_DETAIL.TODO_DETAIL_DATE
    , T_PRIOR.PRIOR_VISIBLESTATUS
    , T_STATUS.STATUS_VISIBLESTATUS
    , T_GOAL.GOAL_VISIBLESTATUS
    , T_TODO_HEADER.TODO_HEADER_VISIBLESTATUS
    , T_TODO_DETAIL.TODO_DETAIL_VISIBLESTATUS
    , T_MEMO.MEMO_VISIBLESTATUS 
FROM
    T_MEMO 
    LEFT OUTER JOIN T_TODO_HEADER 
        USING (TODO_HEADER_ID) 
    LEFT OUTER JOIN T_TODO_DETAIL 
        USING (TODO_DETAIL_ID) 
    LEFT OUTER JOIN T_STATUS 
        USING (STATUS_ID) 
    LEFT OUTER JOIN T_PRIOR 
        USING (PRIOR_ID)
    LEFT OUTER JOIN T_GOAL USING(GOAL_ID)


CREATE VIEW V_TODO_DETAIL AS 
SELECT
    T_TODO_DETAIL.TODO_DETAIL_ID
    , T_TODO_DETAIL.TODO_DETAIL_CONTENT
    , T_TODO_DETAIL.TODO_DETAIL_DATE
    , T_TODO_HEADER.TODO_HEADER_ID
    , T_TODO_HEADER.TODO_HEADER_NAME
    , T_GOAL.GOAL_ID
    , T_GOAL.GOAL_NAME
    , T_PRIOR.PRIOR_ID
    , T_PRIOR.PRIOR_NAME
    , T_PRIOR.PRIOR_SUBNAME
    , T_STATUS.STATUS_ID
    , T_STATUS.STATUS_NAME
    ,T_TODO_DETAIL.TODO_DETAIL_VISIBLESTATUS
    ,T_TODO_HEADER.TODO_HEADER_VISIBLESTATUS
    ,T_GOAL.GOAL_VISIBLESTATUS
    ,T_PRIOR.PRIOR_VISIBLESTATUS
    ,T_STATUS.STATUS_VISIBLESTATUS
FROM
    T_TODO_DETAIL 
    INNER JOIN T_TODO_HEADER 
        ON T_TODO_DETAIL.TODO_HEADER_ID = T_TODO_HEADER.TODO_HEADER_ID 
    INNER JOIN T_GOAL 
        ON T_TODO_HEADER.GOAL_ID = T_GOAL.GOAL_ID 
    INNER JOIN T_PRIOR 
        ON T_TODO_HEADER.PRIOR_ID = T_PRIOR.PRIOR_ID
    INNER JOIN T_STATUS 
        ON T_TODO_DETAIL.STATUS_ID = T_STATUS.STATUS_ID


CREATE VIEW V_TODO_HEADER AS
SELECT
    T_TODO_HEADER.TODO_HEADER_ID
    , T_TODO_HEADER.TODO_HEADER_NAME
    , TODO_HEADER_STARTDATE
    , T_TODO_HEADER.TODO_HEADER_ENDDATE
    , T_GOAL.GOAL_ID
    , T_GOAL.GOAL_NAME
    , T_PRIOR.PRIOR_ID
    , T_PRIOR.PRIOR_NAME
    , T_PRIOR.PRIOR_SUBNAME
    , T_TODO_HEADER.TODO_HEADER_VISIBLESTATUS
    , T_GOAL.GOAL_VISIBLESTATUS
    , T_PRIOR.PRIOR_VISIBLESTATUS 
FROM
    T_TODO_HEADER 
    INNER JOIN T_GOAL 
        ON T_TODO_HEADER.GOAL_ID = T_GOAL.GOAL_ID 
    INNER JOIN T_PRIOR 
        ON T_TODO_HEADER.PRIOR_ID = T_PRIOR.PRIOR_ID





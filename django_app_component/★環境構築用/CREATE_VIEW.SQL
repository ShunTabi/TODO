CREATE VIEW V_TODO AS 
SELECT
    T_TODO.TODO_ID
    , T_TODO.TODO_NAME
    , T_GENRE.GENRE_NAME
    , T_PRIOR.PRIOR_SUBNAME
    , T_TODO.TODO_STARTDATE
    , T_TODO.TODO_ENDDATE
    , T_GENRE.GENRE_VISIBLESTATUS
    , T_PRIOR.PRIOR_VISIBLESTATUS
    , T_TODO.TODO_VISIBLESTATUS 
FROM
    T_TODO 
    INNER JOIN T_GENRE 
        ON T_TODO.GENRE_ID = T_GENRE.GENRE_ID 
    INNER JOIN T_PRIOR 
        ON T_TODO.PRIOR_ID = T_PRIOR.PRIOR_ID
ORDER BY T_TODO.TODO_STARTDATE

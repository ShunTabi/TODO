CREATE TABLE T_GENRE(
GENRE_ID INTEGER PRIMARY KEY AUTOINCREMENT,
GENRE_NAME TEXT NOT NULL UNIQUE,
GENRE_DATE DATE NOT NULL,
GENRE_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_PRIOR(
PRIOR_ID INTEGER PRIMARY KEY AUTOINCREMENT,
PRIOR_NAME TEXT NOT NULL,
PRIOR_SUBNAME TEXT NOT NULL,
PRIOR_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_MEMO(
MEMO_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TODO_DETAIL_ID INTEGER REFERENCES T_TODO_DETAIL(TODO_DETAIL_ID),
MEMO_NOTE TEXT NOT NULL,
MEMO_DATE DATE NOT NULL,
MEMO_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_TODO_HEADER(
TODO_HEADER_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TODO_HEADER_NAME TEXT NOT NULL UNIQUE,
GENRE_ID INTEGER REFERENCES T_GENRE(GENRE_ID),
TODO_HEADER_DATE DATE NOT NULL,
TODO_HEADER_VISIBLESTATUS INTEGER DEFAULT 0
);


CREATE TABLE T_TODO_DETAIL(
TODO_DETAIL_ID INTEGER PRIMARY KEY AUTOINCREMENT,
TODO_HEADER_ID INTEGER REFERENCES T_TODO_HEADER(TODO_HEADER_ID),
TODO_DETAIL_NAME TEXT NOT NULL UNIQUE,
PRIOR_ID INTEGER REFERENCES T_PRIOR(PRIOR_ID),
TODO_DETAIL_STARTDATE DATE NOT NULL,
TODO_DETAIL_ENDDATE DATE NOT NULL,
TODO_DETAIL_VISIBLESTATUS INTEGER DEFAULT 0
);